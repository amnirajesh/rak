package raj.mani.info;
//Rectangle Triangle
public class AdditionOfNumbers {
	public static void main(String[] args) {
		
	for (int i=0;i<10;i++){
		                           for(int k=10;k>i;k--){System.out.print(" ");}
		                           for(int j=0;j<=i;j++){System.out.print("*");}
		                           for(int l=0;l<i;l++){System.out.print("*");} 
		                           for(int m=10;m>=i;m--) {System.out.print(" ");}
    System.out.println();}
	
	
	for(int i=0;i<=10;i++){
                                   for (int k=0;k<i;k++){System.out.print(" ");}
                                   for(int j=10;j>=i;j--) {System.out.print("*");}
                                   for (int l=10;l>i;l--){System.out.print("*");}
                                   for (int m = 0; m <= i; m++){System.out.print(" ");}
        
    System.out.println();}	
}
}